#!/usr/bin/env python3
import csv
import sys
import os
from datetime import datetime
from fpdf import FPDF

# =======================================================================
# [+] ENHANCEMENT: AUTOMATED PDF REPORT GENERATOR
# -----------------------------------------------------------------------
# Reads the CSV output from threat_intel.py and generates an executive
# PDF report suitable for management or Blue Team handoffs.
# =======================================================================

class PDFReport(FPDF):
    def header(self):
        # Format the current date for the header
        current_date = datetime.now().strftime("%B %d, %Y")
        
        # Header formatting
        self.set_font("Arial", "B", 16)
        self.cell(0, 10, "Automated Vulnerability Assessment Report", border=False, ln=True, align="C")
        
        self.set_font("Arial", "I", 10)
        self.cell(0, 8, "Generated by The Scripting Syndicate", border=False, ln=True, align="C")
        
        # [+] ENHANCEMENT: Added date to the report header
        self.set_font("Arial", "", 10)
        self.cell(0, 8, f"Date: {current_date}", border=False, ln=True, align="C")
        self.ln(10)

    def footer(self):
        # Footer formatting (Page numbers)
        self.set_y(-15)
        self.set_font("Arial", "I", 8)
        self.cell(0, 10, f"Page {self.page_no()}", align="C")

def generate_pdf(csv_file, pdf_file):
    if not os.path.exists(csv_file):
        print(f"[-] Error: Could not find '{csv_file}'. Please run threat_intel.py first.")
        sys.exit(1)

    pdf = PDFReport()
    pdf.add_page()
    
    # Read the data from the CSV
    findings = []
    with open(csv_file, mode="r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            findings.append(row)

    if not findings:
        pdf.set_font("Arial", size=12)
        pdf.cell(0, 10, "Scan completed successfully. No vulnerabilities identified.", ln=True)
    else:
        # Group findings by the target machine
        targets = set(f["Target"] for f in findings if "Target" in f)
        
        for target in targets:
            # Executive Summary Statement
            pdf.set_font("Arial", "B", 14)
            pdf.cell(0, 10, f"Target Machine: {target}", ln=True)
            pdf.ln(2)
            
            pdf.set_font("Arial", size=11)
            pdf.multi_cell(0, 7, f"The machine at {target} was scanned, and the following vulnerabilities were identified across its open services. Immediate review and remediation are recommended based on the severity levels associated with these CVEs.")
            pdf.ln(5)

            # Build the Data Table Header
            pdf.set_font("Arial", "B", 10)
            pdf.cell(30, 8, "Port", border=1, align="C")
            pdf.cell(60, 8, "Service", border=1, align="C")
            pdf.cell(60, 8, "CVE ID", border=1, align="C")
            pdf.ln()

            # Populate the Data Table Rows
            pdf.set_font("Arial", size=10)
            target_findings = [f for f in findings if f.get("Target") == target]
            for finding in target_findings:
                port = finding.get("Port", "N/A")
                service = finding.get("Service", "N/A")
                cve = finding.get("CVE ID", "N/A")
                
                pdf.cell(30, 8, port, border=1, align="C")
                pdf.cell(60, 8, service, border=1, align="C")
                pdf.cell(60, 8, cve, border=1, align="C")
                pdf.ln()
                
            pdf.ln(10) # Space before the next target (if multiple)

    # =======================================================================
    # [+] ENHANCEMENT: Final Sign-off and Timestamp
    # =======================================================================
    # We ensure this block is safely outside the 'else' and 'for' loops 
    # so it always prints at the bottom of the document.
    pdf.ln(10)
    pdf.set_font("Arial", "B", 12)
    pdf.cell(0, 10, "Report verified and generated by The Scripting Syndicate:", ln=True)
    
    pdf.set_font("Arial", "", 11)
    pdf.cell(0, 7, "- Devon Brown (Project Lead & Developer)", ln=True)
    pdf.cell(0, 7, "- Shannika Dyer (Documentarian)", ln=True)
    pdf.cell(0, 7, "- Adam Timmons (Tester)", ln=True)
    pdf.cell(0, 7, "- Edy Silveira de Souza (Presenter)", ln=True)

    # Add the exact timestamp to the bottom
    pdf.ln(5)
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    pdf.set_font("Arial", "I", 10)
    pdf.cell(0, 7, f"Official scan concluded on: {timestamp}", ln=True)

    # Output the final file
    pdf.output(pdf_file)
    print(f"[+] Executive Report successfully generated: {pdf_file}")

if __name__ == "__main__":
    # Define our input and output files
    INPUT_CSV = "scan_report.csv"
    OUTPUT_PDF = "Executive_Vulnerability_Report.pdf"
    
    print("\n[*] Initializing The Scripting Syndicate PDF Generator...")
    generate_pdf(INPUT_CSV, OUTPUT_PDF)